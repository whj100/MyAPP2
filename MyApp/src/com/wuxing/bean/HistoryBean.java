package com.wuxing.bean;

public class HistoryBean {
	String id;
	String start;
	String startJingWei;
	String end;
	String endJingWei;
	
	

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getStartJingWei() {
		return startJingWei;
	}

	public void setStartJingWei(String startJingWei) {
		this.startJingWei = startJingWei;
	}

	public String getEndJingWei() {
		return endJingWei;
	}

	public void setEndJingWei(String endJingWei) {
		this.endJingWei = endJingWei;
	}

	public String getStart() {
		return start;
	}

	public void setStart(String start) {
		this.start = start;
	}

	public String getEnd() {
		return end;
	}

	public void setEnd(String end) {
		this.end = end;
	}
	
	
}
